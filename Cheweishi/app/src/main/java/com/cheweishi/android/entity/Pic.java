package com.cheweishi.android.entity;

public class Pic {
	private String name;
	private int w;
	private int h;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getW() {
		return w;
	}

	public void setW(int w) {
		this.w = w;
	}

	public int getH() {
		return h;
	}

	public void setH(int h) {
		this.h = h;
	}

}
